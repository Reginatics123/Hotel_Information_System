        String Nationality = cbNationality.getSelectedItem().toString();
        String Gender = cbGender.getSelectedItem().toString();
        String CheckInDate = tfCheckInDate.getText();
        String DateOfOut = tfDateOfOut.getText();
        String BedType = cbBedType.getSelectedItem().toString();
        String RoomNo = tfRoomNo.getText();
