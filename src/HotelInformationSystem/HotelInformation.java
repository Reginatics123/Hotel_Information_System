    private void tfMobileNoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfMobileNoKeyTyped
        // TODO add your handling code here:
        char ch = evt.getKeyChar();
        if(!(Character.isDigit(ch) || (ch == KeyEvent.VK_SPACE) || ch == KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }
        if(tfMobileNo.getText().length() >= 11) {
            evt.consume();
        } else if (!Character.isDigit(ch)) {
            
            evt.consume();
            
        }
    }//GEN-LAST:event_tfMobileNoKeyTyped

